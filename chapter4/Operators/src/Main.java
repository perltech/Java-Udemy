public class Main {
    public static void main(String[] args) {
        int sum = 1 + 2;
        System.out.println("1 + 2 = " + sum);

        int difference = sum - 1;
        System.out.println("3 - 1 = " + difference);

        int product = difference * 10;
        System.out.println("2 * 10 = " + product);

        int quotient = product / 5;
        System.out.println("20 / 5 = " + quotient);

        int modulus = quotient % 3;
        System.out.println("4 % 3 = " + modulus);
    }
}
